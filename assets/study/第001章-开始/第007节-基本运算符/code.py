# 练习：基本运算符

# 1. 算术运算练习
a = 15
b = 4

# 计算 a 除以 b 的商和余数
quotient = a // b
remainder = a % b

# 2. 比较运算练习
x = 10
y = 20

# 判断 x 是否小于 y
is_smaller = x < y

# 3. 字符串运算练习
first_name = "张"
last_name = "三"

# 拼接姓名
full_name = first_name + last_name

# 4. 逻辑运算练习
age = 18
has_id = True

# 判断是否可以进入（年龄大于等于18且持有身份证）
can_enter = age >= 18 and has_id

# 显示所有结果
quotient
remainder
is_smaller
full_name
can_enter
